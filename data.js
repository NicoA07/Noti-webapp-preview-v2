/* ---------- Data helpers ---------- */

var DataHelpers = {
  find: function (section, id) {
    var arr = AppData[section];
    if (!arr) return null;
    for (var i = 0; i < arr.length; i++) {
      if (arr[i].id === id) return arr[i];
    }
    return null;
  },

  findIndex: function (section, id) {
    var arr = AppData[section];
    if (!arr) return -1;
    for (var i = 0; i < arr.length; i++) {
      if (arr[i].id === id) return i;
    }
    return -1;
  },

  remove: function (section, id) {
    var idx = this.findIndex(section, id);
    if (idx !== -1) return AppData[section].splice(idx, 1)[0];
    return null;
  },

  generateId: function (prefix) {
    return (
      prefix +
      "-" +
      Date.now().toString(36) +
      "-" +
      Math.random().toString(36).substr(2, 4)
    );
  },

  // Find a subtask by id across all items in a section
  findSubtask: function (section, subtaskId) {
    var arr = AppData[section];
    if (!arr) return null;
    for (var i = 0; i < arr.length; i++) {
      var sts = arr[i].subtasks || [];
      for (var j = 0; j < sts.length; j++) {
        if (sts[j].id === subtaskId) return sts[j];
      }
    }
    return null;
  },

  // Recalculate visibleCount heuristic after subtask edits
  refreshVisibleCount: function (item) {
    var total = (item.subtasks || []).length;
    if (total > 3) {
      item.visibleCount = 2;
    } else {
      item.visibleCount = null;
    }
  },

  // Map chip state value ('task'/'keep') to data section key ('tasks'/'keep')
  stateToSection: function (state) {
    return state === "task" ? "tasks" : "keep";
  },

  // Map chip state to saved-data section key
  stateToSavedSection: function (state) {
    return state === "task" ? "savedTasks" : "savedKeep";
  },

  /* ---------- v3 dashboard helpers ---------- */

  // "Morning" (5–12), "Afternoon" (12–18), "Evening" (otherwise)
  dayPart: function () {
    var h = new Date().getHours();
    if (h >= 5 && h < 12) return "Morning";
    if (h >= 12 && h < 18) return "Afternoon";
    return "Evening";
  },

  // Most recent notifications for the summary digest (data is
  // already ordered newest-first), capped at max
  getSummaryNotifs: function (max) {
    return AppData.notifications.slice(0, max);
  },

  // Unique app logos for the digest icon cluster (max 3)
  getSummaryApps: function () {
    var seen = {};
    var apps = [];
    for (var i = 0; i < AppData.notifications.length; i++) {
      var n = AppData.notifications[i];
      if (!seen[n.app]) {
        seen[n.app] = true;
        apps.push(n);
      }
      if (apps.length === 3) break;
    }
    return apps;
  },

  getNotifsByCategory: function (category) {
    return AppData.notifications.filter(function (n) {
      return n.category === category;
    });
  },

  // Counts for the "What's new" chips
  getNewCounts: function () {
    var count = function (arr) {
      return arr.filter(function (item) {
        return item.isNew;
      }).length;
    };
    return { tasks: count(AppData.savedTasks), keep: count(AppData.savedKeep) };
  },

  // Saved tasks that carry unseen LLM updates (amber entries)
  getUpdatedTasks: function () {
    return AppData.savedTasks.filter(function (item) {
      return (item.updates || []).some(function (u) {
        return !u.seen;
      });
    });
  },

  // Pinned items across both saved arrays, tasks first
  getPinned: function () {
    var out = [];
    AppData.savedTasks.forEach(function (item) {
      if (item.pinned) out.push({ section: "savedTasks", item: item });
    });
    AppData.savedKeep.forEach(function (item) {
      if (item.pinned) out.push({ section: "savedKeep", item: item });
    });
    return out;
  },

  // All new items (isNew: true) across tasks + keep, with section info
  getNewItems: function () {
    var items = [];
    AppData.savedTasks.forEach(function (item) {
      if (item.isNew) items.push({ section: "savedTasks", item: item });
    });
    AppData.savedKeep.forEach(function (item) {
      if (item.isNew) items.push({ section: "savedKeep", item: item });
    });
    return items;
  },

  // Single merged "What's new" feed: new items awaiting review first
  // (they need an explicit Approve/Edit/Delete decision), then already-
  // applied updates (informational only). Used by the dashboard carousel.
  getActivityEntries: function () {
    var entries = [];
    this.getNewItems().forEach(function (e) {
      entries.push({ kind: "new", section: e.section, item: e.item });
    });
    this.getUpdatedTasks().forEach(function (item) {
      entries.push({ kind: "update", section: "savedTasks", item: item });
    });
    return entries;
  },

  // getActivityEntries(), narrowed by the dashboard's New/Updated filter
  // chips. Both off (the default) shows everything.
  getVisibleActivityEntries: function () {
    var entries = this.getActivityEntries();
    var filters = UIState.dashboard.activityFilters;
    if (filters.new || filters.update) {
      entries = entries.filter(function (e) {
        return filters[e.kind];
      });
    }
    return entries;
  },

  // v3: "confirm" an item — clear isNew flag (items are already in saved arrays)
  confirmItem: function (section, id, newState) {
    var item = this.find(section, id);
    if (!item) {
      // Try saved arrays directly
      item = this.find("savedTasks", id) || this.find("savedKeep", id);
    }
    if (item) {
      item.isNew = false;
      item.state = newState;
    }
  },

  // Find the notification object by id
  findNotification: function (notifId) {
    for (var i = 0; i < AppData.notifications.length; i++) {
      if (AppData.notifications[i].id === notifId) return AppData.notifications[i];
    }
    return null;
  },

  // Collect all unseen changes across an item's updates, with source info
  getUnseenChangeSummary: function (item) {
    var unseen = (item.updates || []).filter(function (u) { return !u.seen; });
    var allChanges = [];
    var sources = [];
    var seenSources = {};
    unseen.forEach(function (u) {
      u.changes.forEach(function (c) { allChanges.push(c); });
      if (u.sourceNotifId && !seenSources[u.sourceNotifId]) {
        seenSources[u.sourceNotifId] = true;
        sources.push({ notifId: u.sourceNotifId, app: u.sourceApp, time: u.time });
      }
    });
    return { changes: allChanges, sources: sources, latest: unseen[unseen.length - 1] };
  },

  // Short preview text for collapsed update card
  describeChangesPreview: function (changes) {
    var labels = changes.map(function (c) {
      if (c.type === "date") return "Date changed";
      if (c.type === "subtask-added") return "Sub-task added";
      if (c.type === "subtask-removed") return "Sub-task removed";
      if (c.type === "location") return "Venue added";
      return "Updated";
    });
    // Deduplicate
    var seen = {};
    var unique = [];
    labels.forEach(function (l) {
      if (!seen[l]) { seen[l] = true; unique.push(l); }
    });
    return unique.join(", ");
  },

  // Human-readable line for one update change entry
  describeChange: function (change) {
    if (change.type === "date") {
      return "Date changed: " + change.from + " → " + change.to;
    }
    if (change.type === "subtask-added") {
      return "Sub-task added: “" + change.label + "”";
    }
    if (change.type === "subtask-removed") {
      return "Sub-task removed: “" + change.label + "”";
    }
    return "Updated";
  },
};
