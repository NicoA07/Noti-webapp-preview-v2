/* ============================================================
   data.js — Application state (v3 "Dashboard" concept)
   ============================================================
   All card and notification data lives here, separate from
   rendering and behavior. Three global objects:

     AppData   — the "database": saved tasks/keep, notifications
     UIState   — transient UI state: active screen, dashboard
                 expand states, what's being edited
     DataHelpers — CRUD + dashboard utilities used by app.js

   v3 model changes vs v1/v2:
   - No inbox/confirm flow. LLM output lands directly in
     savedTasks / savedKeep with isNew: true (auto-apply).
   - Accepted tasks can receive LLM updates: the item data is
     already updated (auto-apply), and item.updates[] records
     what changed so the UI can show a diff.
   - Notifications carry category: "primary" | "social" for the
     sidebar categorization.

   No DOM access in this file. Pure data.
   ============================================================ */

